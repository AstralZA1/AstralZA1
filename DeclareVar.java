public class DeclareVar {
    public static void main(String[]args){
        int num1 = 6;
        int num2 = 4;
        float dec = 12.5f;
        String phrase = "Java Programming";
        System.out.println("Integer : " +(num1 + num2) );
        System.out.println("Float : " + dec);
        System.out.println("String: "+ phrase);
    }
}
